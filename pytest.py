class Character:

    def __init__(self, image, x, y):
        self.image = pygame.image.load(image)
        self.height = self.image.get_height()
        self.x = x
        self.y = y
        self.can_jump = False
        self.vel_y = 1.00001
        self.posX = self.x

    def draw(self):
        screen.blit(self.image, (self.x, self.y))

    def gravity(self):
        time.sleep(0.001)
        self.y += self.vel_y
        if int(self.y + self.height) >= world.rectsY:
            self.y = world_y - self.height
            self.can_jump = True

    def move(self):
        keys = pygame.key.get_pressed()
        if keys[pygame.K_SPACE] and self.can_jump:
            self.can_jump = False
            self.y -= 20
        if keys[pygame.K_q]:
            self.x -= 600 * dt
        if keys[pygame.K_d]:
            self.x += 600 * dt

